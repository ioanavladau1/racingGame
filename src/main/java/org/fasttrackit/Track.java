package org.fasttrackit;

public class Track {

    private String name;
    private double leght;

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public double getLeght() {
        return leght;
    }

    public void setLeght(double leght) {
        this.leght = leght;
    }

    @Override
    public String toString() {
        return super.toString();
    }
}
